#include <stdio.h>

int main() {
  printf("Hello World!\n"); // Prints "Hello World!" with a new line
  printf("I am learning C.\n"); // Prints "I am learning C." with a new line
  printf("And it is awesome!\n"); // Prints "And it is awesome!" with a new line

// Additional lines with my information
  printf("\nHello World!.\n");
  printf("I am learning C\n");
  printf("And it is awesome!\n");


  printf("Hello World!\n\n\n\n"); // Prints "Hello World!" followed by two new lines
  printf("I am learning C."); // Prints "I am learning C."

  return 0;
}
